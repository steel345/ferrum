/**
 * Downloads all Minecraft 1.20.1 item textures from InventivetalentDev's
 * minecraft-assets GitHub repo, saves them to public/mc-textures/
 * and writes src/data/allItems.js with the complete item list.
 *
 * Run with: node scripts/download-textures.mjs
 */

import https from 'https'
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.join(__dirname, '..')
const TEX_DIR = path.join(ROOT, 'public', 'mc-textures')
const ITEM_DIR = path.join(TEX_DIR, 'item')
const BLOCK_DIR = path.join(TEX_DIR, 'block')

const TEX_BASE = 'https://raw.githubusercontent.com/InventivetalentDev/minecraft-assets/1.20.1/assets/minecraft/textures'
const ITEMS_URL = 'https://raw.githubusercontent.com/PrismarineJS/minecraft-data/master/data/pc/1.20/items.json'

fs.mkdirSync(ITEM_DIR, { recursive: true })
fs.mkdirSync(BLOCK_DIR, { recursive: true })

function fetchJSON(url) {
  return new Promise((resolve, reject) => {
    https.get(url, { headers: { 'User-Agent': 'ferrum-texture-downloader' } }, res => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        return fetchJSON(res.headers.location).then(resolve).catch(reject)
      }
      let data = ''
      res.on('data', c => data += c)
      res.on('end', () => {
        try { resolve(JSON.parse(data)) } catch { reject(new Error('Bad JSON')) }
      })
    }).on('error', reject)
  })
}

function fetchBinary(url) {
  return new Promise((resolve, reject) => {
    https.get(url, { headers: { 'User-Agent': 'ferrum-texture-downloader' } }, res => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        return fetchBinary(res.headers.location).then(resolve).catch(reject)
      }
      if (res.statusCode !== 200) return resolve(null)
      const chunks = []
      res.on('data', c => chunks.push(c))
      res.on('end', () => resolve(Buffer.concat(chunks)))
    }).on('error', () => resolve(null))
  })
}

async function tryDownload(name, type) {
  const url = `${TEX_BASE}/${type}/${name}.png`
  const buf = await fetchBinary(url)
  if (!buf || buf.length < 100) return false
  const dest = path.join(TEX_DIR, type, `${name}.png`)
  fs.writeFileSync(dest, buf)
  return true
}

// sleep to avoid hammering GitHub
const sleep = ms => new Promise(r => setTimeout(r, ms))

async function main() {
  console.log('Fetching item list...')
  const items = await fetchJSON(ITEMS_URL)
  console.log(`Found ${items.length} items`)

  const results = []
  let done = 0
  const CONCURRENCY = 8

  async function processItem(item) {
    const name = item.name
    // try item texture first, then block texture
    let texType = null
    if (await tryDownload(name, 'item')) texType = 'item'
    else if (await tryDownload(name, 'block')) texType = 'block'

    results.push({
      id: `minecraft:${name}`,
      label: item.displayName || name.replace(/_/g, ' ').replace(/\b\w/g, c => c.toUpperCase()),
      tex: texType ? `${texType}/${name}.png` : null,
    })

    done++
    if (done % 50 === 0) {
      const found = results.filter(r => r.tex).length
      console.log(`  ${done}/${items.length} — ${found} textures found`)
    }
  }

  // Process in batches
  for (let i = 0; i < items.length; i += CONCURRENCY) {
    const batch = items.slice(i, i + CONCURRENCY)
    await Promise.all(batch.map(processItem))
    await sleep(100) // small delay between batches
  }

  // Write allItems.js
  const found = results.filter(r => r.tex).length
  console.log(`\nDone! ${found}/${results.length} textures downloaded.`)

  const js = `// Auto-generated by scripts/download-textures.mjs
// ${results.length} items, ${found} with textures
export const ALL_MC_ITEMS = ${JSON.stringify(results, null, 2)}
`
  fs.writeFileSync(path.join(ROOT, 'src', 'data', 'allItems.js'), js)
  console.log('Written src/data/allItems.js')
}

main().catch(e => { console.error(e); process.exit(1) })
